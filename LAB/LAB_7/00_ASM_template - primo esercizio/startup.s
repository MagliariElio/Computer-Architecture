; Reset Handler

value		RN 	R0		;value of array							variable and argument
amount		RN	R1		;quantity of the value (ingredient)		variable and result

quantity	RN	R2		;quantity table							variable and argument
calories	RN	R3		;ingredient - calories table			variable and argument

len			RN 	R4		;number of ingredients					variable
index		RN	R5		;array index							variable
tot			RN	R11		;total value of calories				variable

Reset_Handler   PROC
                EXPORT  Reset_Handler             [WEAK]                                            
                LDR     R0, =Reset_Handler

				; your code here		
				LDR calories, =Ingredient_calories
				LDR quantity, =Ingredient_quantity
				LDRB len, Num_ingredients
				
				;LSL len, #3								;len *= 8
				MOV index, #0								;index = 0
				MOV tot, #0
loop_main
				LDR value, [calories, index, LSL #2]		;value = calories[index]	id
				ADD index, #1
				
				BL Finder_value
				
				LDR value, [calories, index, LSL #2]		;amount = calories[index]	value
				ADD index, #1
				
				MLA tot, value, amount, tot					;tot = (value * amount) + tot
				
				CMP index, len, LSL #1						;if(index < len) goto loop
				BLO loop_main
				
				BX      R0
                ENDP


;	@parameter: 	value (element of ingredient calories array)
;	@return:		amount (element found in ingredient quantity array)
Finder_value	PROC
				PUSH {LR, len, index, value, quantity}
				
				MOV index, #0								;index = 0
loop
				CMP index, len, LSL #1						;if(index >= len) goto exit
				BHS exit
				LDR amount, [quantity, index, LSL #2]		;amount = quantity[index]
				CMP value, amount							;if(value != amount) goto LOOP
				ADD index, #2								;index += 2
				BNE loop
				
				SUB index, #1
				LDR amount, [quantity, index, LSL #2]		;value = quantity of that key
				
exit
				POP {PC, len, index, value, quantity}
				ENDP


